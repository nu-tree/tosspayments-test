/**
 * 약관 시드 데이터 생성
 */
export async function seedTerms(prisma: PrismaClient) {
  console.log('\n\n\n🌱 약관 데이터 입력 시작');
  console.log(`📊 총 ${termsSeeds.length}개의 약관 데이터를 처리합니다.`);
  await new Promise((resolve) => setTimeout(resolve, 500));

  for (const seed of termsSeeds) {
    console.log(`처리 중: ${seed.key} - ${seed.name}`);
    try {
      // 이미 해당 키의 약관이 있는지 확인
      const existing = await prisma.siteTermsSetting.findFirst({
        where: { key: seed.key },
        orderBy: { createdAt: 'desc' },
      });

      if (existing) {
        console.log(`ℹ️  스킵: ${seed.key} - 이미 존재합니다.`);
        continue;
      }

      const result = await prisma.siteTermsSetting.create({
        data: seed,
      });
      console.log(`✅ 성공: ${result.id} (${seed.name})`);
    } catch (error) {
      console.error(`❌ 실패: ${seed.key}`, error);
    }
  }
  console.log('🌱 약관 데이터 입력 완료\n');
  await new Promise((resolve) => setTimeout(resolve, 500));
}

